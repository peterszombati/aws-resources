import {StringProperty} from "../StringProperty"


type Properties = {
  ResourceArn: StringProperty
  Policy: Record<string, any> | StringProperty
  PolicyId?: StringProperty
  PolicyHash?: StringProperty
}

export const AWSSSMResourcePolicy = ({
                                       ResourceName,
                                       DependsOn,
                                       Properties,
                                     }: {
  ResourceName: string
  DependsOn?: string | string[]
  Properties: Record<string, any> & Properties
}) => ({
  Resources: {
    [ResourceName]: {
      Type: 'AWS::SSM::ResourcePolicy',
      DependsOn,
      Properties,
    }
  },
  Outputs: {
    [ResourceName]: {
      Value: {
        "Ref": ResourceName,
      },
      Export: {
        Name: {
          "Fn::Sub": "stack:${AWS::StackName}:" + ResourceName
        }
      }
    }
  }
})