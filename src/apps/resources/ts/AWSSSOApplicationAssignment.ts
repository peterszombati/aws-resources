import {StringProperty} from "../StringProperty"


type Properties = {
  ApplicationArn: StringProperty
  PrincipalType: (string | "USER" | "GROUP")
  PrincipalId: StringProperty
}

export const AWSSSOApplicationAssignment = ({
                                              ResourceName,
                                              DependsOn,
                                              Properties,
                                            }: {
  ResourceName: string
  DependsOn?: string | string[]
  Properties: Record<string, any> & Properties
}) => ({
  Resources: {
    [ResourceName]: {
      Type: 'AWS::SSO::ApplicationAssignment',
      DependsOn,
      Properties,
    }
  },
  Outputs: {
    [ResourceName]: {
      Value: {
        "Ref": ResourceName,
      },
      Export: {
        Name: {
          "Fn::Sub": "stack:${AWS::StackName}:" + ResourceName
        }
      }
    }
  }
})