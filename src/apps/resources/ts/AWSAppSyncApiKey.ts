import {StringProperty} from "../StringProperty"


type Properties = {
  ApiKey?: StringProperty
  Description?: StringProperty
  ApiKeyId?: StringProperty
  Expires?: number
  Arn?: StringProperty
  ApiId: StringProperty
}

export const AWSAppSyncApiKey = ({
                                   ResourceName,
                                   DependsOn,
                                   Properties,
                                 }: {
  ResourceName: string
  DependsOn?: string | string[]
  Properties: Record<string, any> & Properties
}) => ({
  Resources: {
    [ResourceName]: {
      Type: 'AWS::AppSync::ApiKey',
      DependsOn,
      Properties,
    }
  },
  Outputs: {
    [ResourceName]: {
      Value: {
        "Ref": ResourceName,
      },
      Export: {
        Name: {
          "Fn::Sub": "stack:${AWS::StackName}:" + ResourceName
        }
      }
    }
  }
})