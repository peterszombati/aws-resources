import {StringProperty} from "../StringProperty"


type Properties = {
  ViewArn: StringProperty
  ViewVersionArn?: StringProperty
  VersionDescription?: StringProperty
  ViewContentSha256?: StringProperty
  Version?: number
}

export const AWSConnectViewVersion = ({
                                        ResourceName,
                                        DependsOn,
                                        Properties,
                                      }: {
  ResourceName: string
  DependsOn?: string | string[]
  Properties: Record<string, any> & Properties
}) => ({
  Resources: {
    [ResourceName]: {
      Type: 'AWS::Connect::ViewVersion',
      DependsOn,
      Properties,
    }
  },
  Outputs: {
    [ResourceName]: {
      Value: {
        "Ref": ResourceName,
      },
      Export: {
        Name: {
          "Fn::Sub": "stack:${AWS::StackName}:" + ResourceName
        }
      }
    }
  }
})