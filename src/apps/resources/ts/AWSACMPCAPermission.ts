import {StringProperty} from "../StringProperty"


type Properties = {
  Actions: StringProperty[]
  CertificateAuthorityArn: StringProperty
  Principal: StringProperty
  SourceAccount?: StringProperty
}

export const AWSACMPCAPermission = ({
                                      ResourceName,
                                      DependsOn,
                                      Properties,
                                    }: {
  ResourceName: string
  DependsOn?: string | string[]
  Properties: Record<string, any> & Properties
}) => ({
  Resources: {
    [ResourceName]: {
      Type: 'AWS::ACMPCA::Permission',
      DependsOn,
      Properties,
    }
  },
  Outputs: {
    [ResourceName]: {
      Value: {
        "Ref": ResourceName,
      },
      Export: {
        Name: {
          "Fn::Sub": "stack:${AWS::StackName}:" + ResourceName
        }
      }
    }
  }
})